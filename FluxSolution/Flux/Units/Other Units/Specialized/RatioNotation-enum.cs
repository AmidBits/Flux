namespace Flux
{
  namespace Units
  {
    public enum RatioNotation
    {
      /// <summary>As a ratio colon (U+2236), e.g. "1:2".</summary>
      AcolonB,
      /// <summary>With a ratio slash (U+2044), e.g. "1/2".</summary>
      AslashB,
      /// <summary>As textual "A to B", e.g. "1 to 2".</summary>
      AtoB,
    }
  }
}
